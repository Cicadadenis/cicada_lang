class Токен:
    def __init__(self, тип, значение, строка=0):
        self.тип = тип
        self.значение = значение
        self.строка = строка
    def __repr__(self):
        return f"({self.тип}, {self.значение!r})"
    def __eq__(self, other):
        if isinstance(other, tuple):
            return (self.тип, self.значение) == other
        return NotImplemented
    def __getitem__(self, i):
        return (self.тип, self.значение)[i]

def токенизировать(код, файл='<код>'):
    токены = []
    i = 0
    нстр = 1
    while i < len(код):
        c = код[i]

        if c in ' \t\r':
            i += 1
            continue

        if c == '\n':
            токены.append(Токен('НОВСТРОКА', '\n', нстр))
            нстр += 1
            i += 1
            continue

        if c == '#':
            while i < len(код) and код[i] != '\n':
                i += 1
            continue

        if c == '"':
            i += 1
            s = ''
            while i < len(код) and код[i] != '"':
                if код[i] == '\\' and i+1 < len(код):
                    i += 1
                    if код[i] == 'n': s += '\n'
                    elif код[i] == 't': s += '\t'
                    elif код[i] == '"': s += '"'
                    else: s += код[i]
                else:
                    s += код[i]
                i += 1
            i += 1
            токены.append(Токен('СТРОКА', s, нстр))
            continue

        if c.isdigit():
            n = ''
            while i < len(код) and (код[i].isdigit() or код[i] == '.'):
                n += код[i]
                i += 1
            токены.append(Токен('ЧИСЛО', float(n) if '.' in n else int(n), нстр))
            continue

        if c.isalpha() or c == '_' or ord(c) > 127:
            w = ''
            while i < len(код) and (код[i].isalnum() or код[i] == '_' or ord(код[i]) > 127):
                w += код[i]
                i += 1
            тип = 'КС' if w in КЛЮЧЕВЫЕ_СЛОВА else 'ИМЯ'
            токены.append(Токен(тип, w, нстр))
            continue

        if код[i:i+2] in ('==', '!=', '<=', '>=', '+=', '-=', '*=', '/=', '**'):
            токены.append(Токен('ОП', код[i:i+2], нстр))
            i += 2
            continue

        if c in '+-*/=<>()[]{},:%.':
            токены.append(Токен('ОП', c, нстр))
            i += 1
            continue

        i += 1

    токены.append(Токен('КОНЕЦ', None, нстр))
    return токены


