def форматировать(v):
    if isinstance(v, bool): return 'истина' if v else 'ложь'
    if v is None: return 'ничто'
    if isinstance(v, float) and v.is_integer(): return str(int(v))
    if isinstance(v, list): return '[' + ', '.join(форматировать(x) for x in v) + ']'
    return str(v)

