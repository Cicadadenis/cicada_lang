def запустить_файл(путь):
    try:
        with open(путь, encoding='utf-8') as f:
            код = f.read()
    except FileNotFoundError:
        print(f"Ошибка: файл '{путь}' не найден")
        sys.exit(1)
    токены = токенизировать(код, путь)
    интерп = Интерпретатор(токены, путь)
    try:
        интерп.запустить()
    except Exception as e:
        print(f"\nОшибка: {e}")
        sys.exit(1)

