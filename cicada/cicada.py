def _среднее(л):
    if not л: raise Exception("Список пуст")
    return sum(л) / len(л)

def _плоский(л):
    результат = []
    for x in л:
        if isinstance(x, list):
            результат.extend(_плоский(x))
        else:
            результат.append(x)
    return результат

