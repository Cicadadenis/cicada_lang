class Интерпретатор:
    def __init__(self, токены, файл='<код>'):
        self.токены = токены
        self.поз = 0
        self.файл = файл
        self.переменные = [{}]
        self.функции = {}

    def текущий(self):
        return self.токены[self.поз]

    def съесть(self, тип=None, значение=None):
        tok = self.текущий()
        if тип and tok.тип != тип:
            raise Exception(f"[строка {tok.строка}] Ожидался {тип}, получен {tok.значение!r}")
        if значение and tok.значение != значение:
            raise Exception(f"[строка {tok.строка}] Ожидалось '{значение}', получено '{tok.значение}'")
        self.поз += 1
        return tok

    def пропустить_новстроки(self):
        while self.текущий().тип == 'НОВСТРОКА':
            self.поз += 1

    def получить(self, имя):
        for область in reversed(self.переменные):
            if имя in область:
                return область[имя]
        raise Exception(f"Переменная '{имя}' не определена")

    def установить(self, имя, значение):
        for область in reversed(self.переменные):
            if имя in область:
                область[имя] = значение
                return
        self.переменные[-1][имя] = значение

    # ---- ВЫРАЖЕНИЯ ----

    def выражение(self):
        return self.логическое()

    def логическое(self):
        лево = self.сравнение()
        while self.текущий().значение in ('и', 'или'):
            оп = self.съесть().значение
            право = self.сравнение()
            лево = (лево and право) if оп == 'и' else (лево or право)
        return лево

    def сравнение(self):
        if self.текущий().значение == 'не':
            self.съесть()
            return not self.сравнение()
        лево = self.сложение()
        while self.текущий().тип == 'ОП' and self.текущий().значение in ('==','!=','<','>','<=','>='):
            оп = self.съесть().значение
            право = self.сложение()
            if оп == '==': лево = лево == право
            elif оп == '!=': лево = лево != право
            elif оп == '<':  лево = лево < право
            elif оп == '>':  лево = лево > право
            elif оп == '<=': лево = лево <= право
            elif оп == '>=': лево = лево >= право
        return лево

    def сложение(self):
        лево = self.умножение()
        while self.текущий().тип == 'ОП' and self.текущий().значение in ('+', '-'):
            оп = self.съесть().значение
            право = self.умножение()
            лево = лево + право if оп == '+' else лево - право
        return лево

    def умножение(self):
        лево = self.степень()
        while self.текущий().тип == 'ОП' and self.текущий().значение in ('*', '/', '%'):
            оп = self.съесть().значение
            право = self.степень()
            if оп == '*': лево = лево * право
            elif оп == '/': лево = лево / право
            elif оп == '%': лево = int(лево) % int(право)
        return лево

    def степень(self):
        лево = self.унарное()
        if self.текущий().значение == '**':
            self.съесть()
            право = self.степень()
            return лево ** право
        return лево

    def унарное(self):
        if self.текущий().значение == '-':
            self.съесть()
            return -self.первичное_с_индексом()
        return self.первичное_с_индексом()

    def первичное_с_индексом(self):
        узел = self.первичное()
        while self.текущий().значение == '[':
            self.съесть('ОП', '[')
            индекс = self.выражение()
            self.съесть('ОП', ']')
            try:
                узел = узел[int(индекс) if isinstance(индекс, (int,float)) else индекс]
            except IndexError:
                raise Exception(f"Индекс {int(индекс)} за пределами")
        return узел

    def первичное(self):
        tok = self.текущий()

        if tok.тип == 'ЧИСЛО':
            self.съесть()
            return tok.значение

        if tok.тип == 'СТРОКА':
            self.съесть()
            return tok.значение

        if tok.значение == 'истина':
            self.съесть(); return True
        if tok.значение == 'ложь':
            self.съесть(); return False
        if tok.значение == 'ничто':
            self.съесть(); return None

        if tok.значение == '[':
            self.съесть('ОП', '[')
            элементы = []
            self.пропустить_новстроки()
            while self.текущий().значение != ']':
                элементы.append(self.выражение())
                self.пропустить_новстроки()
                if self.текущий().значение == ',':
                    self.съесть()
                    self.пропустить_новстроки()
            self.съесть('ОП', ']')
            return элементы

        if tok.тип == 'ИМЯ':
            имя = self.съесть().значение
            if self.текущий().значение == '(':
                self.съесть('ОП', '(')
                аргументы = []
                self.пропустить_новстроки()
                while self.текущий().значение != ')':
                    аргументы.append(self.выражение())
                    self.пропустить_новстроки()
                    if self.текущий().значение == ',':
                        self.съесть()
                        self.пропустить_новстроки()
                self.съесть('ОП', ')')
                if имя in ВСТРОЕННЫЕ:
                    try:
                        fn = ВСТРОЕННЫЕ[имя]
                        if callable(fn):
                            return fn(*аргументы)
                        raise Exception(f"'{имя}' — константа, не функция")
                    except Exception as e:
                        raise Exception(f"'{имя}': {e}")
                if имя in self.функции:
                    return self._запустить_функцию(имя, аргументы)
                raise Exception(f"Функция '{имя}' не определена")
            # Константы из ВСТРОЕННЫЕ (не функции)
            if имя in ВСТРОЕННЫЕ and not callable(ВСТРОЕННЫЕ[имя]):
                return ВСТРОЕННЫЕ[имя]
            return self.получить(имя)

        if tok.значение == '(':
            self.съесть()
            значение = self.выражение()
            self.съесть('ОП', ')')
            return значение

        raise Exception(f"[строка {tok.строка}] Неожиданный токен: {tok.значение!r}")

    # ---- ИНСТРУКЦИИ ----

    def блок(self):
        self.пропустить_новстроки()
        while (self.текущий().тип != 'КОНЕЦ' and
               self.текущий().значение not in ('конец', 'иначе', 'иначе_если', 'ошибка')):
            self.инструкция()
            self.пропустить_новстроки()

    def инструкция(self):
        tok = self.текущий()

        # пусть x = / += / -= ...
        if tok.значение == 'пусть':
            self.съесть()
            имя = self.съесть('ИМЯ').значение
            if self.текущий().значение == '[':
                self.съесть('ОП', '[')
                индекс = self.выражение()
                self.съесть('ОП', ']')
                self.съесть('ОП', '=')
                значение = self.выражение()
                self.получить(имя)[int(индекс)] = значение
                self.пропустить_новстроки()
                return
            оп = self.съесть().значение
            значение = self.выражение()
            if оп == '=': self.установить(имя, значение)
            elif оп == '+=': self.установить(имя, self.получить(имя) + значение)
            elif оп == '-=': self.установить(имя, self.получить(имя) - значение)
            elif оп == '*=': self.установить(имя, self.получить(имя) * значение)
            elif оп == '/=': self.установить(имя, self.получить(имя) / значение)
            self.пропустить_новстроки()
            return

        # вывод a, b, c
        if tok.значение == 'вывод':
            self.съесть()
            части = [self.выражение()]
            while self.текущий().значение == ',':
                self.съесть()
                части.append(self.выражение())
            print(' '.join(форматировать(p) for p in части))
            self.пропустить_новстроки()
            return

        # если / иначе_если / иначе / конец
        if tok.значение == 'если':
            self.съесть()
            условие = self.выражение()
            self.пропустить_новстроки()
            if условие:
                self.переменные.append({})
                self.блок()
                self.переменные.pop()
                while self.текущий().значение == 'иначе_если':
                    self.съесть(); self.выражение()
                    self.пропустить_новстроки(); self._пропустить_блок()
                if self.текущий().значение == 'иначе':
                    self.съесть(); self.пропустить_новстроки(); self._пропустить_блок()
            else:
                self._пропустить_блок()
                выполнено = False
                while self.текущий().значение == 'иначе_если':
                    self.съесть()
                    усл2 = self.выражение()
                    self.пропустить_новстроки()
                    if усл2 and not выполнено:
                        выполнено = True
                        self.переменные.append({})
                        self.блок()
                        self.переменные.pop()
                    else:
                        self._пропустить_блок()
                if self.текущий().значение == 'иначе':
                    self.съесть(); self.пропустить_новстроки()
                    if not выполнено:
                        self.переменные.append({})
                        self.блок()
                        self.переменные.pop()
                    else:
                        self._пропустить_блок()
            self.съесть('КС', 'конец')
            self.пропустить_новстроки()
            return

        # пока условие ... конец
        if tok.значение == 'пока':
            self.съесть()
            поз_условия = self.поз
            try:
                while True:
                    self.поз = поз_условия
                    условие = self.выражение()
                    self.пропустить_новстроки()
                    if not условие:
                        self._пропустить_блок()
                        break
                    self.переменные.append({})
                    try:
                        self.блок()
                    except ПродолжитьЦикл:
                        pass
                    self.переменные.pop()
            except СтопЦикла:
                if self.текущий().значение != 'конец':
                    self._пропустить_блок()
            self.съесть('КС', 'конец')
            self.пропустить_новстроки()
            return

        # для x в итерируемое ... конец
        if tok.значение == 'для':
            self.съесть()
            имя = self.съесть('ИМЯ').значение
            self.съесть('КС', 'в')
            итерируемое = self.выражение()
            self.пропустить_новстроки()
            тело = self._собрать_блок()
            тело_токены = тело + [Токен('КОНЕЦ', None)]
            try:
                for элемент in итерируемое:
                    суб = Интерпретатор(тело_токены)
                    суб.функции = self.функции
                    суб.переменные = self.переменные + [{имя: элемент}]
                    try:
                        суб.запустить()
                    except ПродолжитьЦикл:
                        pass
            except СтопЦикла:
                pass
            self.съесть('КС', 'конец')
            self.пропустить_новстроки()
            return

        # функция имя(параметры) ... конец
        if tok.значение == 'функция':
            self.съесть()
            имя = self.съесть('ИМЯ').значение
            self.съесть('ОП', '(')
            параметры = []
            while self.текущий().значение != ')':
                параметры.append(self.съесть('ИМЯ').значение)
                if self.текущий().значение == ',':
                    self.съесть()
            self.съесть('ОП', ')')
            self.пропустить_новстроки()
            тело = self._собрать_блок()
            self.функции[имя] = (параметры, тело)
            self.съесть('КС', 'конец')
            self.пропустить_новстроки()
            return

        # вернуть
        if tok.значение == 'вернуть':
            self.съесть()
            if self.текущий().тип in ('НОВСТРОКА', 'КОНЕЦ'):
                raise Возврат(None)
            raise Возврат(self.выражение())

        # стоп / продолжить
        if tok.значение == 'стоп':
            self.съесть(); self.пропустить_новстроки(); raise СтопЦикла()
        if tok.значение == 'продолжить':
            self.съесть(); self.пропустить_новстроки(); raise ПродолжитьЦикл()

        # импорт "файл"
        if tok.значение == 'импорт':
            self.съесть()
            путь = self.выражение()
            if not путь.endswith('.cicada'):
                путь += '.cicada'
            if not os.path.exists(путь):
                raise Exception(f"Файл '{путь}' не найден")
            with open(путь, encoding='utf-8') as f:
                код = f.read()
            токены = токенизировать(код, путь)
            суб = Интерпретатор(токены, путь)
            суб.переменные = self.переменные
            суб.функции = self.функции
            суб.запустить()
            self.пропустить_новстроки()
            return

        # попытка ... ошибка [имя] ... конец
        if tok.значение == 'попытка':
            self.съесть(); self.пропустить_новстроки()
            тело = self._собрать_блок()
            имя_ошибки = None
            тело_ошибки = []
            if self.текущий().значение == 'ошибка':
                self.съесть()
                if self.текущий().тип == 'ИМЯ':
                    имя_ошибки = self.съесть().значение
                self.пропустить_новстроки()
                тело_ошибки = self._собрать_блок()
            self.съесть('КС', 'конец')
            self.пропустить_новстроки()
            try:
                суб = Интерпретатор(тело + [Токен('КОНЕЦ', None)])
                суб.функции = self.функции
                суб.переменные = self.переменные
                суб.запустить()
            except (СтопЦикла, ПродолжитьЦикл, Возврат):
                raise
            except Exception as e:
                if тело_ошибки:
                    доп = {имя_ошибки: str(e)} if имя_ошибки else {}
                    суб2 = Интерпретатор(тело_ошибки + [Токен('КОНЕЦ', None)])
                    суб2.функции = self.функции
                    суб2.переменные = self.переменные + [доп]
                    суб2.запустить()
            return

        # x = ... или x += ... или x[i] = ...
        if tok.тип == 'ИМЯ':
            следующий = self.токены[self.поз + 1]
            if следующий.значение in ('=', '+=', '-=', '*=', '/='):
                имя = self.съесть().значение
                оп = self.съесть().значение
                значение = self.выражение()
                if оп == '=': self.установить(имя, значение)
                elif оп == '+=': self.установить(имя, self.получить(имя) + значение)
                elif оп == '-=': self.установить(имя, self.получить(имя) - значение)
                elif оп == '*=': self.установить(имя, self.получить(имя) * значение)
                elif оп == '/=': self.установить(имя, self.получить(имя) / значение)
                self.пропустить_новстроки()
                return
            if следующий.значение == '[':
                имя = self.съесть().значение
                self.съесть('ОП', '[')
                индекс = self.выражение()
                self.съесть('ОП', ']')
                self.съесть('ОП', '=')
                значение = self.выражение()
                self.получить(имя)[int(индекс)] = значение
                self.пропустить_новстроки()
                return

        self.выражение()
        self.пропустить_новстроки()

    def _пропустить_блок(self):
        глубина = 1
        while глубина > 0:
            tok = self.текущий()
            if tok.тип == 'КОНЕЦ': break
            if tok.значение in ('если', 'пока', 'для', 'функция', 'попытка'):
                глубина += 1
            elif tok.значение == 'конец':
                глубина -= 1
                if глубина == 0: break
            elif tok.значение in ('иначе', 'иначе_если', 'ошибка') and глубина == 1:
                break
            self.поз += 1

    def _собрать_блок(self):
        нач = self.поз
        self._пропустить_блок()
        return self.токены[нач:self.поз]

    def _запустить_функцию(self, имя, аргументы):
        параметры, тело = self.функции[имя]
        if len(аргументы) != len(параметры):
            raise Exception(f"'{имя}': ожидалось {len(параметры)} аргументов, получено {len(аргументы)}")
        суб = Интерпретатор(тело + [Токен('КОНЕЦ', None)])
        суб.функции = self.функции
        суб.переменные = [dict(zip(параметры, аргументы))]
        try:
            суб.запустить()
        except Возврат as r:
            return r.значение
        return None

    def запустить(self):
        self.пропустить_новстроки()
        while self.текущий().тип != 'КОНЕЦ':
            self.инструкция()


